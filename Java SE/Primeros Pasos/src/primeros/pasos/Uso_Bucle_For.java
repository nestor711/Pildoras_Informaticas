public class Uso_Bucle_For {

	public static void main(String[] args) {

		// Bucle o Ciclo For (Incrementando)
		for (int i = 0; i < 10; i++) {

			System.out.println("Néstor");
		}
	}

	public static void main2(String[] args) {

		// Bucle o Ciclo For (Decrementando)
		for (int i = 10; i > 0; i--) {

			System.out.println("Néstor");
		}
	}

}
